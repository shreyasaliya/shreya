#Arithmetic operator
print("arithmetic operators")
print("5 + 6 is", 5+6)
print("5 - 6 is", 5-6)
print("5 * 6 is", 5*6)
print("5 / 6 is", 5/6)
print("5 % 3 is", 5%3)
print("5 ** 3 is", 5**3)
print("15 // 6 is", 15//6)

#Assignment operators
print("assignment operators")
x = 5
print(x)
x +=7
print(x)

#comparison operators
print("comparison operators")
i = 8
print(i==5)
print(i==8)
print(i>=5)
print(i<8)

#logical operators
print("logical operators")
a = True
b = False

print(a and b) #if both are true returns true
print(a or b) #if any one is true returns true

#identity operators
print("identity operators")
print(a is b)
print(a is not b)
print(5 != 5)
print(5 == 6)

#membership operators
print("membership operators")
list = [3,3,2,33,45,67,89]
print(33 in list)
print(34 not in list)

#bitwise operators
print("bitwise operators")
# 0 = 00
# 1 = 01
# 2 = 10
# 3 = 11

print(0 & 1)
print(0 | 1)
print(0 | 3)
